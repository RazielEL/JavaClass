package com.syntax.class03;

public class relationalOperators {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 16;
		
		System.out.println(num1>num2);
		System.out.println(num1==num2);
		System.out.println(num1<num2);

		boolean result = (num1!=num2);
		
		System.out.println(result);
		
		
		
	}

}
